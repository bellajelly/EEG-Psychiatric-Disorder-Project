# CLASS 1: EEG Classifier

# Initializing the EEG Classifier class: Random Forest MVP
# Data is scaled to keep each diagnositic group balanced
# Data labels are then encoded 1 - 7

class EEG_classifier:
    def __init__(self):
        self.model = RandomForestClassifier(
        n_estimators=100,
        max_depth=10,
        min_samples_leaf=5,
        class_weight='balanced',
        random_state=25
      )
        self.scaler = StandardScaler()
        self.le = LabelEncoder()
        self.data = None

    # Dataframe is loaded when user is prompted for url
    # Data is loaded, unnamed columns columns are dropped and education/IQ were filled
    # Exception is thrown if the url is faulty, or the shape of the data is incorrect

    def load_data(self, url):
        try:
            self.data = pd.read_excel(url)
            self.data = self.data.drop(columns=[col for col in self.data.columns if 'Unnamed' in col])
            self.data = self.data.drop(columns=['no.', 'eeg.date'], errors='ignore')
            self.data['education'] = self.data['education'].fillna(self.data['education'].median())
            self.data['IQ'] = self.data['IQ'].fillna(self.data['IQ'].median())
            return self.data

        except Exception as e:
            print(f"Error loading data: {e}")
            return None

    # data is validated by checking 3 things:
    # presence of main.disorder column
    # presence of at least one frequency band column
    # presence of missing data, which is appended to the empty list

    def validate_data(self, data):
        missing = []
        if 'main.disorder' not in data.columns:
            missing.append('main.disorder')

        for band in ['delta', 'theta', 'alpha', 'beta', 'gamma']:
            band_columns = [column for column in data.columns if band in column.lower()]
            if len(band_columns) == 0:
                missing.append(band)

        if len(missing) == 0:
            return True, None

        else:
            return False, f"Missing required data columns: {', '.join(missing)}"

    # Encodes labels, one-hot encodes sex
    # Splits then scales data, before returning 2 training and 2 test sets

    def preprocess_data(self, data):
        data = pd.get_dummies(data, columns=['sex'], drop_first=True).copy()
        data['disorder_label'] = self.le.fit_transform(data['main.disorder'])

        X = data.drop(columns=['main.disorder', 'disorder_label', 'specific.disorder'])
        y = data['disorder_label']

        X_train, X_test, y_train, y_test = train_test_split(
        X, y, test_size=0.2, random_state=25, stratify=y)

        X_train_scaled = self.scaler.fit_transform(X_train)
        X_test_scaled = self.scaler.transform(X_test)

        return X_train_scaled, X_test_scaled, y_train, y_test

    # Train self.model using selected model
    # Generate predictions
    # Get classification_report as a dictionary
    # Filter disorder classes only, excluding accuracy, macro avg, etc

    def train_and_predict(self, X_train, X_test, y_train, y_test):
        self.model.fit(X_train, y_train)
        y_prediction = self.model.predict(X_test)
        report = classification_report(
        y_test, y_prediction, 
        labels=list(range(len(self.le.classes_))),
        target_names=list(self.le.classes_),
        output_dict=True)
        #class_names = list(self.le.classes_)
        results = {disorder: metrics 
        for disorder, metrics in report.items() 
        if disorder in self.le.classes_}
        #print("Results keys:", list(results.keys()))
        #print("Report keys:", list(report.keys()))
        #print("LE classes:", list(self.le.classes_))
        return results

    # Call load_data(url) —> if it returns None, return (False, error message)
    # Call validate_data(data) —> if valid is False, return (False, message)
    # Call preprocess_data(data) inside a try/except block
    # Call train_and_predict with the four arrays: X_train, y_train, X_test, y_test
    # Return classification report with specific metrics

    def run_classifier(self, url):
        data = self.load_data(url)
        if data is None:
            return False, "Could not load data from URL provided"

        valid, message = self.validate_data(data)
        if not valid:
            return False, message

        try:
            X_train, X_test, y_train, y_test = self.preprocess_data(data)
            results = self.train_and_predict(X_train, X_test, y_train, y_test)
        
        except Exception as e:
            return False, f"Processing error: {str(e)}"

        return True, results

# CLASS 3: Unit Tests for load_data and validate_data methods

# EEG_classifier unittests, running an example valid dataframe and an example invalid dataframe
# valid dataframe contains main.disorder column and at least one column of all 5 frequency bands
# invalid dataframe contains an invalid column name and incorrect values

class TestEEGClassifier(unittest.TestCase):
    def setUp(self):
      self.classifier = EEG_classifier()

    def test_load_data_valid(self):
      url = "https://raw.githubusercontent.com/bellajelly/EEG-Psychiatric-Disorder-Project/refs/heads/main/EEG.machinelearing_data_BRMH.xlsx"
      result = self.classifier.load_data(url)
      self.assertIsNotNone(result)

    def test_load_data_invalid(self):
      result = self.classifier.load_data('not_a_url')
      self.assertIsNone(result)

    def test_validate_data_valid(self):
      valid_data = pd.DataFrame({
    'main.disorder': ['Schizophrenia', 'Depression'],
    'delta.F3.F4': [0.5, 0.3],
    'theta.F3.F4': [0.4, 0.2],
    'alpha.F3.F4': [0.6, 0.1],
    'beta.F3.F4': [0.3, 0.5],
    'gamma.F3.F4': [0.2, 0.4]
})
      is_valid, message = self.classifier.validate_data(valid_data)
      self.assertTrue(is_valid)
      self.assertIsNone(message)

    def test_validate_data_invalid(self):
      invalid_data = pd.DataFrame({
    'incorrect_column': [1, 2, 3]
})
      is_valid, message = self.classifier.validate_data(invalid_data)
      self.assertFalse(is_valid)
      self.assertIsNotNone(message)

