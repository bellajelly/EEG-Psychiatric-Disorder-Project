    def _on_run(self):
        # Called when run button is pressed
        # Clears previous state
        self._show_error("")
        self._clear_results()
        self._show_status("Running classifier... this may take a moment.")
        self.run_button.config(state='disabled')
        self.root.update()

        url = self.url_entry.get().strip()

        # Input validation
        if not url:
            self._show_error("Please enter a dataset URL.")
            self._show_status("")
            self.run_button.config(state='normal')
            return

        if not url.startswith('http'):
            self._show_error("URL must start with http:// or https://")
            self._show_status("")
            self.run_button.config(state='normal')
            return

        # Run the classifier
        success, payload = self.classifier.run_classifier(url)

        self.run_button.config(state='normal')

        if not success:
            self._show_error(payload)
            self._show_status("")
        else:
            self._show_status("Classification complete.")
            self._display_results(payload)

    def _display_results(self, results):
        # Displays metrics via treeview
        self._clear_results()

        for disorder, metrics in results.items():
            precision = f"{metrics['precision']:.3f}"
            recall = f"{metrics['recall']:.3f}"
            f1 = f"{metrics['f1-score']:.3f}"
            support = int(metrics['support'])
            self.tree.insert('', 'end', values=(disorder, precision, recall, f1, support))

    def _show_error(self, message):
        # Displays error message
        self.error_label.config(text=message)

    def _show_status(self, message):
        # Displays status message
        self.status_label.config(text=message)

    def _clear_results(self):
        # Clears all rows from treeview
        for row in self.tree.get_children():
            self.tree.delete(row)

